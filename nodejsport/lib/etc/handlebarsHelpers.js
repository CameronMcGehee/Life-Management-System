const { options } = require("../../routes/admin");

module.exports = {

// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// GENERAL ELEMENTS HELPERS
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------

	selectOptions: function (selected, optons) {
		return options
		.fn(this)
		.replace(
			new RegExp(' value="' + selected + '"'),
			'$& selected="selected"'
		)
		.replace(
			new RegExp('>' + selected + '</option>'),
			' selected="selected"$&'
		)
	},

// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// LOGICAL HELPERS
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------


	ifeq: function(a, b, options){
		if (a === b) {
			return options.fn(this);
			}
		return options.inverse(this);
	}


// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// LISTS/TABLES
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------

}
